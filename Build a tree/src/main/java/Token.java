

public class Token {
    String tipo;
    int valor;
    
    public void set_tipo(String tipo){
        this.tipo = tipo;
    }
    
    public void set_valor(int valor){
        this.valor = valor;
    }
    
    public String get_tipo(){
        return tipo;
    }
    
    public int get_valor(){
        return valor;
    }
}
